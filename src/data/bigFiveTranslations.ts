/**
 * Returns the translated question text for a given question ID and language.
 * Falls back to Portuguese (the original text) if no translation is found.
 */
export function getQuestionText(questionId: string, originalText: string, language: string): string {
  const lang = language?.split('-')[0] || 'pt';
  
  if (lang === 'pt') return originalText;
  
  if (lang === 'en') {
    return questionsEN[questionId] || originalText;
  }
  
  if (lang === 'es') {
    return questionsES[questionId] || originalText;
  }
  
  return originalText;
}
