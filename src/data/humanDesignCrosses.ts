/**
 * Translate an Incarnation Cross name from Portuguese to the target language.
 * Falls back to the original value if no translation is found.
 */
export function translateCross(ptValue: string | null | undefined, language: string): string {
  if (!ptValue) return 'N/A';
  if (language === 'pt') return ptValue;
  
  const lang = language === 'en' ? 'en' : language === 'es' ? 'es' : null;
  if (!lang) return ptValue;
  
  const translation = crossTranslations[ptValue];
  return translation ? translation[lang] : ptValue;
}
